    private void validateFields() {
        LocalDate birthDate = Utils.toLocalDate(dpBirthDate.getDate());
        LocalDate startDate = Utils.toLocalDate(dpStart.getDate());
        LocalDate endDate = Utils.toLocalDate(dpEnd.getDate());
        try {
            double salary = Double.parseDouble(txtSalary.getText());
            if (!txtName.getText().isEmpty()
                    && birthDate != null && birthDate.isBefore(LocalDate.now())
                    && txtPhone.getText().matches("[0-9]{10}")
                    && salary > 0
                    && startDate != null
                    && (endDate == null || (!startDate.isAfter(endDate)))) {
                btnAction.setEnabled(true);
            } else {
                btnAction.setEnabled(false);
            }
        } catch (Exception ex) {
            btnAction.setEnabled(false);
        }
        if (txtName.getText().equals("")) {
            Name.setVisible(true);
        } else {
            Name.setVisible(false);
        }
        if (dpBirthDate.getDate() == null) {
            Date.setVisible(true);
        } else {
            Date.setVisible(false);
        }
        if (dpStart.getDate() == null) {
            Start.setVisible(true);
        } else {
            Start.setVisible(false);
        }
        if (dpEnd.getDate() == null) {
            End.setVisible(true);
        } else {
            End.setVisible(false);
        }
        if (txtPhone.getText().equals("")) {
            Phone.setVisible(true);
        } else {
            Phone.setVisible(false);
        }
        if (txtSalary.getText().equals("")) {
            Monthly.setVisible(true);
        } else {
            Monthly.setVisible(false);
        }
    }

