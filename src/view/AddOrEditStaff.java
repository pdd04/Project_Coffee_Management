    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        List.of("Male", "Female").forEach(cbxSex::addItem);
        List.of("Barista", "Server", "Chef", "Manager", "Counter Service", "Shipper").forEach(cbxPosition::addItem);

        if (staffId != -1) {
            lblTitle.setText("Edit Staff");
            lblTitle.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/view edit delete product.png")));

            Staff staff = staffDao.getById(staffId);
            txtName.setText(staff.getFullName());
            cbxSex.setSelectedItem(staff.getSex());
            dpBirthDate.setDate(Utils.toDate(staff.getBirthDate()));
            txtPhone.setText(staff.getPhoneNumber());
            cbxPosition.setSelectedItem(staff.getPosition());
            txtSalary.setText("%.2f".formatted(staff.getMonthlySalary()));
            dpStart.setDate(Utils.toDate(staff.getStartDate()));
            dpEnd.setDate(Utils.toDate(staff.getEndDate()));
        } 
    }//GEN-LAST:event_formComponentShown

